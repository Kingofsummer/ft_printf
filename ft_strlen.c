#include "ft_printf.h"

size_t	ft_strlen(const char *s, t_struct *list)
{
	int i;

	i = 0;
	if (!s)
		return (i);
	while (s[i] != '\0')
	{
		list->i++;
		i++;
	}
	return (i);
}
